nome = "\tedivaldo junior\n"

print("sem modificações: ")
print(nome)

print("usando lstrip(): ")
print(nome.lstrip()) #lstrip() remove os espaços em branco no lado esquerdo da string

print("usando rstrip(): ")
print(nome.rstrip()) #rstrip() remove os espaços em branco no lado direito da string

print("\nusando strip(): ")
print(nome.strip()) #strip() remove os espaços em branco nos dois lados da string
